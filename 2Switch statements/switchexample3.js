let day=new Date().getDay()
switch(day){
    case 1:
        console.log("hey its on a monday");
        break;
    case 2:
        console.log("leo ni tuesday");
        break;
    case 3:
        console.log("Leo ni wednesday");
        break;
    case 4:
        console.log("Leo ni TBT");
        break;
    case 5:
        console.log("Furahi Day");
    case 6:
        console.log("leo ni sato");
        break;
    case 0:
        break;
    default:
        console.log("Invalid Day");
}
