'use strict'
const fruits=[`Banana`,`kiwi`]
/*we use the push function to add an object to the array
In this case we will have added mango to the array when we use the arrayname.push function
*/
fruits.push(`mango`)
console.log(fruits)
/*we Can also use another push method which adds the pushed object to be the first in
array and that is the unshift command*/
const matunda=[`Mandizi`,`Maembe`,`kiwi`]
matunda.unshift(`mananasi`)
console.log(matunda)